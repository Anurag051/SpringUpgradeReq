package com.anurag.beans;

/**
 * @author ANURAG
 * @version 1.0
 */
public class Student {
	private int sid;
	private String sname;
	
	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

}
